///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// Imports
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

const mongoose = require('mongoose');


///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// Document Schema for MongoDB
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

const foodSchema = new mongoose.Schema({
    userDayId: {
      type: String,
      unique: true,
      required: false,
      sparse: true
    },
    foodName: {
      type: String,
      required: true,
      maxlength: [20, 'Food name can only be up to 20 characters']
    },
    calorieAmount: {
      type: Number,
      min: [0, 'Amount of calories must be higher than -1'],
      required: true
    }
  });
  

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// Exports
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

const Food = mongoose.model('food', foodSchema);

module.exports = Food;